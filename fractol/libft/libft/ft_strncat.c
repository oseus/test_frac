#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int				a;
	unsigned int	b;

	a = 0;
	b = 0;
	while (s1[a])
		a++;
	while (b < n && s2[b])
		s1[a++] = s2[b++];
	s1[a] = '\0';
	return (s1);
}
