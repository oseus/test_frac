#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*current;

	if (lst && f)
	{
		current = lst;
		while (current != NULL)
		{
			(*f)(current);
			current = current->next;
		}
	}
}
